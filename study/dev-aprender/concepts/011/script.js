// -- 011 - Operadores de Igualdade -------- //

/*
    • Igualdade Estrita
        - === : Compara o tipo além dos valores, sendo o mais recomendado

    • Igualdade Solta
        - == : Compara apenas os valores convertendo os tipos
*/

console.log(1 === 1);
console.log(1 === "1")

console.log(1 == 1)
console.log(1 == "1")