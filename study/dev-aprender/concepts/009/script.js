let infoSalary = 1000;
console.log(infoSalary + infoSalary);
console.log(infoSalary - infoSalary);
console.log(infoSalary * infoSalary);
console.log(infoSalary / infoSalary);
console.log(infoSalary ** 3);
console.log("")

let infoAge = 18;
console.log(infoAge++);
console.log(infoAge);

console.log(++infoAge);
console.log(infoAge);